                <h1>Obtenção das informações pessoais</h1>
            
                <p>São coletadas informações pessoais capazes de identificar os usuários quando estes:<br>

                    a) se cadastram no site Mundo Pet;<br>

                        b) ao entrarem em contato com a Central de Atendimento da Petlove.<br>

                        3.2 Todos os dados pessoais coletados serão incorporados à base de dados da Mundo Pet,
                        cabendo ao Mundo Pet o armazenamento destes dados.<br>

                        3.3 A Mundo Pet poderá confirmar os dados pessoais informados pelo usuário consultando entidades públicas,
                        companhias especializadas, banco de dados lícitos, para o que fica expressamente autorizado. <br>
                        Tal atitude, contudo, não cessa nem diminui a responsabilidade do usuário quanto à exatidão e veracidade 
                        dos dados que fornecer motivo pelo qual a Mundo Pet poderá,
                        a seu exclusivo critério, suspender e/ou cancelar o cadastro do usuário, a qualquer momento, caso detecte qualquer inexatidão.<br>

                    3.4 A Mundo Pet poderá permanecer com o registro de dados e informações dos visitantes pelo período máximo de 5 (cinco) anos 
                    para fins de auditoria e preservação de direitos, com a faculdade de excluí-los definitivamente segundo sua conveniência em
                    período menor.</p>
